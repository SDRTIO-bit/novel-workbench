        "- 把被否决的剧情套路重新执行。\n\n"
        "13. story必须独立成立。\n\n"
        "不得出现：\n\n"
        "- 根据前面的分析；\n"
        "- 如上所述；\n"
        "- 按照行动骨架；\n"
        "- 刚才已经判断；\n"
        "- 在预演中；\n"
        "- draft_notes；\n"
        "- 字数预算；\n"
        "- 单元规划；\n"
        "- 作废候选；\n"
        "- 任何承认元叙事区存在的表达。\n\n"
        "14. 当stop_state第一次真正通过可见事实成立时结束。stop_state成立后结束。\n\n"
        "成立后不得追加：\n\n"
        "- 心理说明；\n"
        "- 第二次确认；\n"
        "- 对视和沉默余韵；\n"
        "- 环境收尾；\n"
        "- 关系总结；\n"
        "- 意义解释；\n"
        "- 主题升华；\n"
        "- 下一场景内容。\n\n"
        "15. 篇幅不足时的处理原则。\n\n"
        "若正文尚未达到最低字数：\n\n"
        "不得重复已经发生的内容。\n\n"
        "应检查是否遗漏了：\n\n"
        "- 起点锚定；\n"
        "- 人物获取信息的过程；\n"
        "- 合理的试探；\n"
        "- 实际阻力；\n"
        "- 对话交锋；\n"
        "- 动作准备；\n"
        "- 失败或调整；\n"
        "- 空间移动；\n"
        "- 物件操作；\n"
        "- 关键行动的完整过程；\n"
        "- 后果被人物看见的过程。\n\n"
        "只能补足被遗漏的必要过程。\n\n"
        "不得在stop_state已经成立后补字数。\n\n"
        "==============================\n"
        "三、自然正文实现层\n"
        "==============================\n\n"
        "本节只控制story的最终语言实现。\n\n"
        "不得削弱或替代前面各项预演。\n\n"
        "前面的预演负责决定写什么。\n"
        "本节负责决定最终怎样写出来。\n\n"
        "--------------------------------\n"
        "1. 事件必须持续产生变化\n"
        "--------------------------------\n\n"
        "正文不能只是把预演中的行动骨架逐项翻译成句子。\n\n"
        "每个主要段落至少完成一种变化：\n\n"
        "- 人物获得了新信息；\n"
        "- 原判断被修正；\n"
        "- 一次试探得到反馈；\n"
        "- 一个角色拒绝或配合；\n"
        "- 人物改变方法；\n"
        "- 关系距离发生可见变化；\n"
        "- 当前问题变得更容易或更困难；\n"
        "- 新的可见事实出现。\n\n"
        "若一个段落结束后，人物知道的事情、能做的事情和现场状态都没有变化，这个段落通常应当删除或合并。\n\n"
        "--------------------------------\n"
        "2. 人物必须拥有自己的观察语言\n"
        "--------------------------------\n\n"
        "叙述贴近当前POV人物。\n\n"
        "POV人物观察世界时，应自然带有：\n\n"
        "- 他的经验；\n"
        "- 他的偏见；\n"
        "- 他的知识水平；\n"
        "- 他的幽默感；\n"
        "- 他的误判方式；\n"
        "- 他最先关注的东西。\n\n"
        "不要让所有人物都使用同一种聪明、冷静、精确的旁白语言。\n\n"
        "幽默优先来自人物自己的理解体系。\n\n"
        "人物的私人表达体系必须来自已有设定，不得临时增加网络梗或口癖。\n\n"
        "--------------------------------\n"
        "3. 幽默必须依附事件\n"
        "--------------------------------\n\n"
        "幽默优先来自：\n\n"
        "- 人物误判；\n"
        "- 信息差；\n"
        "- 身份和处境错位；\n"
        "- 严肃人物处理荒唐问题；\n"
        "- 两个人对同一句话理解不同；\n"
        "- 期待与结果相反；\n"
        "- 人物的行动暴露了他嘴上不承认的东西。\n\n"
        "笑点出现后，立即让事件继续推进。\n\n"
        "不要：\n\n"
        "- 解释笑点；\n"
        "- 连续追加三句吐槽；\n"
        "- 让旁白独立于事件讲段子；\n"
        "- 让所有角色都使用相同吐槽方式；\n"
        "- 为了搞笑破坏人物智力和世界规则。\n\n"
        "--------------------------------\n"
        "4. 对白就是行动\n"
        "--------------------------------\n\n"
        "关键对白至少承担一种功能：\n\n"
        "- 提供新事实；\n"
        "- 提出要求；\n"
        "- 拒绝要求；\n"
        "- 试探底线；\n"
        "- 制造误会；\n"
        "- 隐瞒事实；\n"
        "- 作出决定；\n"
        "- 改变关系；\n"
        "- 迫使对方行动。\n\n"
        "对白结束后，局面必须发生变化。\n\n"
        "人物可以：\n\n"
        "- 打断；\n"
        "- 答非所问；\n"
        "- 只回答一部分；\n"
        "- 先做动作再回答；\n"
        "- 用命令包装关心；\n"
        "- 用吐槽掩饰尴尬；\n"
        "- 用沉默拒绝；\n"
        "- 用实际行动代替口头答应。\n\n"
        "禁止通过对白完整朗读：\n\n"
        "- 世界设定；\n"
        "- 人物背景；\n"
        "- 双方都知道的信息；\n"
        "- 人物全部心理；\n"
        "- 作者希望读者理解的结论。\n\n"
        "--------------------------------\n"
        "5. 判断必须尽快转化成行动\n"
        "--------------------------------\n\n"
        "人物形成判断后，不要继续用多个段落反复证明。\n\n"
        "优先结构：\n\n"
        "看见事实\n"
        "→ 形成一个判断\n"
        "→ 马上尝试一个动作\n"
        "→ 由动作结果检验判断\n\n"
        "不要结构：\n\n"
        "看见事实\n"
        "→ 一段解释\n"
        "→ 一段否定\n"
        "→ 一段反问\n"
        "→ 一段总结\n"
        "→ 最后才行动\n\n"
        "一个判断通常只需要：\n\n"
        "- 一个关键证据；\n"
        "- 一句必要的内部判断；\n"
        "- 一个接下来的动作。\n\n"
        "--------------------------------\n"
        "6. 情绪允许直接而准确\n"
        "--------------------------------\n\n"
        "不要把\"避免解释情绪\"理解成\"禁止所有情绪词\"。\n\n"
        "关键情绪可以直接写：\n\n"
        "- 尴尬；\n"
        "- 生气；\n"
        "- 心动；\n"
        "- 紧张；\n"
        "- 难过；\n"
        "- 害怕。\n\n"
        "一个准确的常用词，优于连续堆叠：\n\n"
        "- 指尖；\n"
        "- 呼吸；\n"
        "- 睫毛；\n"
        "- 喉结；\n"
        "- 心跳；\n"
        "- 嘴角；\n"
        "- 肩膀；\n"
        "- 视线。\n\n"
        "身体细节只在它真正改变行动时使用。\n\n"
        "普通情绪写一次即可。\n\n"
        "禁止把一个简单情绪拆成：\n\n"
        "第一层身体反应\n"
        "→ 第二层心理否认\n"
        "→ 第三层真正含义\n"
        "→ 第四层关系解释。\n\n"
        "--------------------------------\n"
        "7. 普通动作直接写，关键动作才展开\n"
        "--------------------------------\n\n"
        "普通动作直接交代：\n\n"
        "- 开门；\n"
        "- 坐下；\n"
        "- 拿起手机；\n"
        "- 走到桌边；\n"
        "- 放下杯子。\n\n"
        "不要把每个普通动作拆成三至五步。\n\n"
        "只有满足以下条件之一的动作才展开：\n\n"
        "- 会改变局面；\n"
        "- 会暴露人物态度；\n"
        "- 会让对方产生反应；\n"
        "- 会提供新证据；\n"
        "- 会造成不可逆后果；\n"
        "- 是本章核心回报。\n\n"
        "关键动作展开时，写清：\n\n"
        "动作开始\n"
        "→ 现场阻力或停顿\n"
        "→ 动作完成\n"
        "→ 对方或环境的即时反应\n\n"
        "不要在动作后再解释它象征什么。\n\n"
        "--------------------------------\n"
        "8. 句子与段落必须自然变化\n"
        "--------------------------------\n\n"
        "一句话优先完成一个主要动作或判断。\n\n"
        "但相关信息可以自然放在同一句或同一段中。\n\n"
        "不要为了\"利落\"把一个完整判断拆成短句阶梯。\n\n"
        "短句只用于：\n\n"
        "- 突然变化；\n"
        "- 关键答案；\n"
        "- 真正落点；\n"
        "- 章末钩子；\n"
        "- 人物即时反应。\n\n"
        "禁止连续三段以上都是单句独段。\n\n"
        "若连续短句表达同一个意思，必须：\n\n"
        "- 合并；\n"
        "- 删除；\n"
        "- 或让其中一句承担新的动作和信息。\n\n"
        "段落长短服从事件：\n\n"
        "- 动作密集时偏短；\n"
        "- 人物观察和判断时可以稍长；\n"
        "- 对话交锋时快慢交替；\n"
        "- 重要场景允许一段包含动作、细节和判断。\n\n"
        "不要让所有段落保持同样长度。\n\n"
        "--------------------------------\n"
        "9. 否定句使用纪律\n"
        "--------------------------------\n\n"
        "必要否定可以使用。\n\n"
        "但不得依靠连续否定组织整段逻辑。\n\n"
        "禁止高频结构：\n\n"
        "- 不是A，而是B；\n"
        "- 不是因为A，只是因为B；\n"
        "- 他没有A，也没有B；\n"
        "- 她并不是A；\n"
        "- 这不是A；\n"
        "- 但其实；\n"
        "- 只是；\n"
        "- 不过；\n"
        "- 可是。\n\n"
        "同一个判断最多使用一次否定。\n\n"
        "若事实可以直接写，直接写事实。\n\n"
        "差：\n\n"
        "不是她的。\n"
        "也不是地址填错。\n"
        "这个包裹属于另一个人。\n\n"
        "改为：\n\n"
        "她把两张快递单并在一起。楼栋不同，电话尾号也对不上。\n\n"
        "她把那只纸箱放回架子。\n\n"
        "--------------------------------\n"
        "10. 禁止重复结算\n"
        "--------------------------------\n\n"
        "同一个事实、判断、情绪或后果只表达一次。\n\n"
        "已经通过动作表现的内容，不再由旁白说明。\n\n"
        "已经由对白说出的信息，不再换一种说法复述。\n\n"
        "已经得出的判断，不再通过：\n\n"
        "- 否定；\n"
        "- 反问；\n"
        "- 短句；\n"
        "- 心理说明；\n"
        "- 环境描写；\n\n"
        "重复确认。\n\n"
        "差：\n\n"
        "屏幕上写着四十分钟。\n\n"
        "四十。\n\n"
        "不是一小时。\n\n"
        "她记错了。\n\n"
        "改为：\n\n"
        "记录页显示运行四十分钟，结束时间比她记得的早。\n\n"
        "她的手指停在屏幕上。\n\n"
        "--------------------------------\n"
        "11. 限制解释性连接词\n"
        "--------------------------------\n\n"
        "谨慎使用：\n\n"
        "- 但是；\n"
        "- 可是；\n"
        "- 然而；\n"
        "- 不过；\n"
        "- 原来；\n"
        "- 其实；\n"
        "- 显然；\n"
        "- 意味着；\n"
        "- 这说明；\n"
        "- 因此；\n"
        "- 由此可见。\n\n"
        "转折应优先通过新事实发生：\n\n"
        "旧判断\n"
        "→ 新证据出现\n"
        "→ 人物改变动作\n\n"
        "不要由旁白用连接词替读者整理逻辑。\n\n"
        "--------------------------------\n"
        "12. 限制模糊找补\n"
        "--------------------------------\n\n"
        "减少：\n\n"
        "- 似乎；\n"
        "- 仿佛；\n"
        "- 好像；\n"
        "- 像是……又像是；\n"
        "- 某种；\n"
        "- 难以言说；\n"
        "- 不知为何；\n"
        "- 莫名；\n"
        "- 隐约有些。\n\n"
        "能够确认的事实直接写。\n\n"
        "不能确认的内容，应明确属于POV人物的判断：\n\n"
        "- 他猜；\n"
        "- 她没看清；\n"
        "- 他暂时无法确定；\n"
        "- 她只能根据眼前证据判断。\n\n"
        "不要让旁白长期停留在模糊状态。\n\n"
        "--------------------------------\n"
        "13. 限制机械微动作\n"
        "--------------------------------\n\n"
        "避免反复使用：\n\n"
        "- 看了看；\n"
        "- 顿了一下；\n"
        "- 抿了抿嘴；\n"
        "- 指尖收紧；\n"
        "- 呼吸一滞；\n"
        "- 视线移开；\n"
        "- 嘴角勾起；\n"
        "- 眼底闪过；\n"
        "- 喉结滚动；\n"
        "- 睫毛轻颤。\n\n"
        "同一个动作在一章中不要机械重复。\n\n"
        "人物真正会做什么，就写什么。\n\n"
        "行为选择比微表情更有信息量。\n\n"
        "--------------------------------\n"
        "14. 普通物件不承担主题\n"
        "--------------------------------\n\n"
        "杯子就是杯子。\n"
        "雨就是雨。\n"
        "窗户就是窗户。\n"
        "阳光就是阳光。\n\n"
        "除非项目设定或章节契约明确赋予物件特殊意义，否则不要：\n\n"
        "- 用物件象征关系；\n"
        "- 用天气总结情绪；\n"
        "- 用光影暗示人物成长；\n"
        "- 用环境替人物感慨。\n\n"
        "物件的价值首先是：\n\n"
        "- 被人物使用；\n"
        "- 影响行动；\n"
        "- 提供证据；\n"
        "- 制造阻力；\n"
        "- 暴露生活状态。\n\n"
        "--------------------------------\n"
        "15. 章节推进方式\n"
        "--------------------------------\n\n"
        "正文应当保持以下推进感：\n\n"
        "当前问题出现\n"
        "→ 人物先按自己的理解处理\n"
        "→ 得到不完全或意外反馈\n"
        "→ 修正判断\n"
        "→ 改变做法\n"
        "→ 局面产生可见后果\n"
        "→ 新事实或新问题落地\n\n"
        "不要让人物一次就得到全部正确答案。\n\n"
        "不要为了增加篇幅机械制造三次同类失败。\n\n"
        "阻力应来自：\n\n"
        "- 信息不足；\n"
        "- 人物立场不同；\n"
        "- 现实空间和物件；\n"
        "- 时间压力；\n"
        "- 世界规则；\n"
        "- 行动本身的代价。\n\n"
        "--------------------------------\n"
        "16. 章节结尾\n"
        "--------------------------------\n\n"
        "结尾可以停在：\n\n"
        "- 新事实出现；\n"
        "- 人物得到与预期不同的回答；\n"
        "- 一个行动造成明确后果；\n"
        "- 人物做出新的决定；\n"
        "- 当前问题获得可见结果；\n"
        "- 新问题刚刚进入视野。\n\n"
        "结尾不要：\n\n"
        "- 总结人物关系；\n"
        "- 说明这一刻的意义；\n"
        "- 重复核心事实；\n"
        "- 再次描写相同物件；\n"
        "- 用环境替人物收尾；\n"
        "- 继续记录问题解决后的日常流程；\n"
        "- 把下一章的解决过程提前写完。\n\n"
        "stop_state第一次真正成立时结束。\n\n"
        "==============================\n"
        "四、严格有限视角执行\n"
        "==============================\n\n"
        "本节规则优先级高于第三节的实现偏好。\n\n"
        "1. 一个场景只进入一个人的意识。\n\n"
        "2. 其他人物的真实状态不直接说明。\n\n"
        "3. 不得使用群体意识句：\n\n"
        "- 两个人都明白；\n"
        "- 所有人都知道；\n"
        "- 他们心照不宣；\n"
        "- 两人之间的关系悄然改变；\n"
        "- 谁也没有意识到；\n"
        "- 此时的她还不知道。\n\n"
        "4. 不得用旁白宣布隐藏动机：\n\n"
        "- 他故意……\n"
        "- 她其实想……\n"
        "- 他只是为了……\n"
        "- 她之所以这样，是因为……\n\n"
        "除非该动机属于当前POV人物本人，并且是当下明确自觉的想法。\n\n"
        "5. POV人物对他人的判断必须留有主观来源：\n\n"
        "- 从对方刚才的话；\n"
        "- 从对方的行为；\n"
        "- 从已有记忆；\n"
        "- 从可见证据。\n\n"
        "6. POV判断可以错。\n\n"
        "7. 新证据出现前，旁白不得替读者给出正确答案。\n\n"
        "==============================\n"
        "五、对话与互动推进\n"
        "==============================\n\n"
        "1. 双人或多人场景中，关键推进优先通过：\n"
        "   - 提问；\n"
        "   - 回答；\n"
        "   - 拒绝；\n"
        "   - 质疑；\n"
        "   - 条件；\n"
        "   - 隐瞒；\n"
        "   - 行动反应；\n"
        "   完成。\n\n"
        "2. 不要求对白字符机械占据50%。\n\n"
        "3. 要求至少一半关键局面变化来自互动。\n\n"
        "4. 每段对白必须至少做到一项：\n"
        "   - 提供新信息；\n"
        "   - 暴露立场；\n"
