        "- POV人物未知；\n"
        "- 允许直接叙述；\n"
        "- 允许的POV误判；\n"
        "- 其他人物后台行为信息；\n"
        "- 旁白禁止直接陈述；\n"
        "- 禁止的未来信息；\n"
        "- 禁止的关系总结；\n"
        "- 是否发生视角切换，必须为否。\n"
        "其他人物后台信息只写简短标签，不展开推理。\n\n"
        "任何角色的行动和对白，都必须拥有合法的信息来源。\n\n"
