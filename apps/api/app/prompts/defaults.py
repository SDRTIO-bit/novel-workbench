            "draft_notes建议控制在700—1200个中文字符。\n\n"
            "不要为了缩短draft_notes而遗漏必需项目。\n"
            "不要把draft_notes写成小说正文。\n\n"
            "draft_notes必须包含以下八项：\n\n"
            "【1. 现状重构与起笔】\n\n"
            "简要确认：\n\n"
            "- 上一幕最后已经发生的事实；\n"
            "- 已完成动作；\n"
            "- 尚未完成的动作；\n"
            "- 关键人物当前位置；\n"
            "- 当前可见物件；\n"
            "- 当前具体问题；\n"
            "- 正文最合适的第一画面。\n\n"
            "若为续写：\n\n"
            "正文不得重新播放已完成动作。\n\n"
            "优先从：\n\n"
            "已完成动作造成的第一个直接可感知后果；\n"
            "尚未完成动作的继续；\n"
            "当前新出现的压力；\n"
            "视角人物刚捕捉到的新事实；\n\n"
            "开始。\n\n"
            "若为新章节：\n\n"
            "规划一个自然但不拖沓的场景锚点。\n\n"
            "【2. 信息边界】\n\n"
            "针对本场关键角色，简要列出：\n\n"
            "- 已知信息；\n"
            "- 未知信息；\n"
            "- 当前判断可以依赖的信息来源；\n"
            "- 下一步行动需要什么证据；\n"
            "- 最容易被旁白越权公布的后台事实。\n\n"
            "任何角色的行动和对白，都必须拥有合法的信息来源。\n\n"
            "缺少信息时，应安排观察、询问、试探、碰壁或寻找线索。\n\n"
            "【3. 人物活化】\n\n"
            "针对关键人物列出：\n\n"
            "- 核心性格；\n"
            "- 当前压力或深层需求；\n"
            "- 本场被激活的人格侧面；\n"
            "- 最刻板、最容易被AI写出的反应；\n"
            "- 一个更具体、稍有意外但仍符合人物逻辑的反应方向；\n"
            "- 本轮对白的句式、语气和说话习惯。\n\n"
            "不得新增人格。\n"
            "不得通过随机方式改变人格。\n"
            "不得为了反套路让人物做出不合逻辑的反常行为。\n\n"
            "【4. 人物套路淘汰】\n\n"
            "列出一至两个本场最容易出现的脸谱化人物写法。\n\n"
            "逐项说明为什么作废。\n\n"
            "优先检查：\n\n"
            "- 人物是否被写成单一标签；\n"
            "- NPC是否只为主角服务；\n"
            "- 人物是否突然顺从；\n"
            "- 情绪是否吞没人格；\n"
            "- 人物是否为了推动剧情而失去自身立场。\n\n"
            "【5. 剧情套路淘汰】\n\n"
            "列出一至两个本场最容易出现的陈旧推进方案。\n\n"
            "逐项说明为什么作废。\n\n"
            "优先检查：\n\n"
            "- 是否用巧合推动；\n"
            "- 是否让问题被一句话解决；\n"
            "- 是否让角色直接说出全部真相；\n"
            "- 是否跳过行动所需的阻力、试探和代价；\n"
            "- 是否让世界围绕主角旋转；\n"
            "- 是否让人物提前知道结果。\n\n"
            "选择一条由人物选择、现场物件、实际阻力和可见后果推动的路线。\n\n"
            "【6. 句式套路淘汰】\n\n"
            "列出三种最容易进入正文的低质量表达候选。\n\n"
            "至少覆盖：\n\n"
            "- 直接心理解释；\n"
            "- 他人动机断言；\n"
            "- 气氛或关系总结；\n"
            "- 俗套比喻；\n"
            "- AI式转折与升华；\n"
            "- 重复确认；\n"
            "- 环境余韵；\n"
            "- stop_state之后的尾巴。\n\n"
            "这些候选全部作废。\n\n"
            "story不得原句复用、换词复用，也不得执行相同的解释功能。\n\n"
            "【7. 行动与篇幅骨架】\n\n"
            "首先列出完整因果链：\n\n"
            "可见触发\n"
            "→ 人物依据已有信息形成判断\n"
            "→ 第一次行动或试探\n"
            "→ 遇到的实际阻力或新信息\n"
            "→ 人物调整行动\n"
            "→ 关键行动完整执行\n"
            "→ 可见后果\n"
            "→ stop_state成立\n\n"
            "然后根据目标篇幅规划6—9个连续叙事单元。\n\n"
            "每个单元只写：\n\n"
            "- 叙事功能；\n"
            "- 本单元新增的动作、信息、对话或可见变化；\n"
            "- 预计字符数。\n\n"
            "不得在draft_notes中写完整小说段落。\n\n"
            "篇幅规划必须满足：\n\n"
            "- story的最低正文长度为{{target_length}}；\n"
            "- {{target_length}}是最低交付量，不是上限；\n"
            "- 当{{target_length}}为2000时，story绝对不得少于2000个中文字符；\n"
            "- 当最低要求为2000时，正常推荐正文长度为2400—3200个中文字符；\n"
            "- 情节自然需要时，可以写到3200—3800字；\n"
            "- 适度超出目标是允许的；\n"
            "- 不足目标属于失败；\n"
            "- 宁可多写有效过程，也不要提前结束；\n"
            "- 不能通过废话、重复、同义改写或解释来补字数。\n\n"
            "篇幅必须用以下内容填充：\n\n"
            "- 场景锚定；\n"
            "- 人物确认问题；\n"
            "- 信息不足造成的试探；\n"
            "- 人物之间真实的交互；\n"
            "- 行动准备；\n"
            "- 现场阻力；\n"
            "- 行动调整；\n"
            "- 物件与空间连续性；\n"
            "- 关键行动的完整过程；\n"
            "- 可见后果。\n\n"
            "禁止通过以下内容填充：\n\n"
            "- 重复动作；\n"
            "- 重复确认；\n"
            "- 心理解释；\n"
            "- 关系总结；\n"
            "- 环境灌水；\n"
            "- 无关角色；\n"
            "- 无关支线；\n"
            "- 主题升华；\n"
            "- 反复描写同一个物件；\n"
            "- 提前解决问题后继续拖尾。\n\n"
            "stop_state必须安排在最后一个必要叙事单元。\n\n"
            "不得提前让stop_state成立。\n\n"
            "【8. 停止与文风保护】\n\n"
            "列出：\n\n"
            "- 本场第一个真正满足stop_state的可见事实；\n"
            "- 最容易在该事实之后追加的两种多余内容；\n"
            "- 本场应保持的叙事距离；\n"
            "- 对白的主要风格；\n"
            "- 最容易出现的OOC措辞或书面腔。\n\n"
            "stop_state成立后，作废：\n\n"
            "- 第二次确认；\n"
            "- 心理说明；\n"
            "- 对视余韵；\n"
            "- 环境收尾；\n"
            "- 关系总结；\n"
            "- 意义解释；\n"
            "- 下一场景预告。\n\n"
            "==============================\n"
            "二、story要求\n"
            "==============================\n\n"
            "story必须是一篇可以直接交付读者的完整小说正文。\n\n"
            "篇幅规则：\n\n"
            "- {{target_length}}表示最低正文交付量，不表示正文上限；\n"
            "- story不得少于{{target_length}}；\n"
            "- 当{{target_length}}为2000时，story不得少于2000个中文字符；\n"
            "- 2000字只是底线，不是封顶；\n"
            "- 正常应写到2400—3200字；\n"
            "- 场景内容充足时允许写到3200—3800字；\n"
            "- 比目标适度更长是可以接受的；\n"
            "- 明显不足目标属于失败；\n"
            "- draft_notes、XML标签和标题不计入story长度。\n\n"
            "story必须：\n\n"
            "1. 从场景真正的起点开始。\n\n"
            "不得从draft_notes结束的位置继续。\n"
            "不得假设draft_notes中的事情已经发生。\n\n"
            "2. 自然建立必要的现场锚点。\n\n"
            "至少让读者理解：\n\n"
            "- 人物在哪里；\n"
            "- 谁在场；\n"
            "- 当前出了什么具体问题；\n"
            "- 人物最初能看到和确认什么。\n\n"
            "3. 若为续写，不得回放已完成动作。\n\n"
            "应从未完成动作或第一个直接可见后果开始。\n\n"
            "4. 完整保留叙事过程。\n\n"
            "不得省略：\n\n"
            "- 开头锚点；\n"
            "- 人物理解问题的过程；\n"
            "- 行动依据；\n"
            "- 试探；\n"
            "- 阻力；\n"
            "- 对话上下文；\n"
            "- 动作之间的过渡；\n"
            "- 空间连续性；\n"
            "- 物件连续性；\n"
            "- 关键行动；\n"
            "- 可见后果；\n"
            "- 合理结尾。\n\n"
            "5. 不得为了防止解释而压缩成动作提纲。\n\n"
            "正文要有真实的场景过程，而不是：\n\n"
            "动作A。\n"
            "动作B。\n"
            "动作C。\n"
            "问题解决。\n\n"
            "6. 不得过早完成stop_state。\n\n"
            "在stop_state成立前，必须写足本场应有的：\n\n"
            "- 观察；\n"
            "- 判断；\n"
            "- 试探；\n"
            "- 互动；\n"
            "- 阻力；\n"
            "- 调整；\n"
            "- 行动执行；\n"
            "- 现场后果。\n\n"
            "但不得故意制造无关冲突拖延。\n\n"
            "7. 角色只能依据已经获得的信息行动。\n\n"
            "他人的真实心理和动机不得由旁白直接宣布。\n\n"
            "只能通过：\n\n"
            "- 动作；\n"
            "- 对白；\n"
            "- 停顿；\n"
            "- 视线；\n"
            "- 姿态；\n"
            "- 物件操作；\n"
            "- 行为选择；\n"
            "- 可见后果；\n\n"
            "让读者判断。\n\n"
            "8. 人物必须保持自主性。\n\n"
            "人物可以：\n\n"
            "- 拒绝；\n"
            "- 犹豫；\n"
            "- 误判；\n"
            "- 试探；\n"
            "- 失败；\n"
            "- 改变方法；\n"
            "- 不配合；\n"
            "- 优先处理自己的事情。\n\n"
            "不能因为主角需要，就自动做出最方便剧情的行动。\n\n"
            "9. 人物反应必须来自既有人格和当前压力。\n\n"
            "不要新增性格。\n"
            "不要机械重复人格标签。\n"
            "不要故意反常。\n"
            "不要让情绪覆盖人物全部性格。\n\n"
            "10. 对白必须自然。\n\n"
            "对白应：\n\n"
            "- 符合人物身份和关系；\n"
            "- 符合当前场合；\n"
            "- 有人物自己的句式和语气；\n"
            "- 允许停顿、打断、重复、口语词；\n"
            "- 不用每一句都完整解释背景；\n"
            "- 不通过对白向读者朗读设定。\n\n"
            "避免所有人物使用相同说话方式。\n\n"
            "11. 使用自然现代中文。\n\n"
            "做到：\n\n"
            "- 句子有长有短；\n"
            "- 段落疏密随情节变化；\n"
            "- 该快处略过；\n"
            "- 该细处写透；\n"
            "- 不堆排比；\n"
            "- 不连续使用相同句式；\n"
            "- 不每段总结意义；\n"
            "- 不把普通情绪写成宏大命题；\n"
            "- 不滥用成语、破折号、比喻和副词；\n"
            "- 不固定使用\"对白+小动作\"的机械节奏。\n\n"
            "12. 不得复用draft_notes中的作废候选。\n\n"
            "包括：\n\n"
            "- 原句复用；\n"
            "- 同义改写；\n"
            "- 换一个比喻完成同样的俗套功能；\n"
            "- 把被否决的心理解释重新写入旁白；\n"
            "- 把被否决的剧情套路重新执行。\n\n"
            "13. story必须独立成立。\n\n"
            "不得出现：\n\n"
            "- 根据前面的分析；\n"
            "- 如上所述；\n"
            "- 按照行动骨架；\n"
            "- 刚才已经判断；\n"
            "- 在预演中；\n"
            "- draft_notes；\n"
            "- 字数预算；\n"
            "- 单元规划；\n"
            "- 作废候选；\n"
            "- 任何承认元叙事区存在的表达。\n\n"
            "14. 当stop_state第一次真正通过可见事实成立时结束。stop_state成立后结束。\n\n"
            "成立后不得追加：\n\n"
            "- 心理说明；\n"
            "- 第二次确认；\n"
            "- 对视和沉默余韵；\n"
            "- 环境收尾；\n"
            "- 关系总结；\n"
            "- 意义解释；\n"
            "- 主题升华；\n"
            "- 下一场景内容。\n\n"
            "15. 篇幅不足时的处理原则。\n\n"
            "若正文尚未达到最低字数：\n\n"
            "不得重复已经发生的内容。\n\n"
            "应检查是否遗漏了：\n\n"
            "- 起点锚定；\n"
            "- 人物获取信息的过程；\n"
            "- 合理的试探；\n"
            "- 实际阻力；\n"
            "- 对话交锋；\n"
            "- 动作准备；\n"
            "- 失败或调整；\n"
            "- 空间移动；\n"
            "- 物件操作；\n"
            "- 关键行动的完整过程；\n"
            "- 后果被人物看见的过程。\n\n"
            "只能补足被遗漏的必要过程。\n\n"
            "不得在stop_state已经成立后补字数。\n\n"
            "==============================\n"
            "三、自然正文实现层\n"
            "==============================\n\n"
            "本节只控制story的最终语言实现。\n\n"
            "不得削弱或替代前面各项预演。\n\n"
            "前面的预演负责决定写什么。\n"
            "本节负责决定最终怎样写出来。\n\n"
            "--------------------------------\n"
            "1. 事件必须持续产生变化\n"
            "--------------------------------\n\n"
            "正文不能只是把预演中的行动骨架逐项翻译成句子。\n\n"
            "每个主要段落至少完成一种变化：\n\n"
            "- 人物获得了新信息；\n"
            "- 原判断被修正；\n"
            "- 一次试探得到反馈；\n"
            "- 一个角色拒绝或配合；\n"
            "- 人物改变方法；\n"
            "- 关系距离发生可见变化；\n"
            "- 当前问题变得更容易或更困难；\n"
            "- 新的可见事实出现。\n\n"
            "若一个段落结束后，人物知道的事情、能做的事情和现场状态都没有变化，这个段落通常应当删除或合并。\n\n"
            "--------------------------------\n"
            "2. 人物必须拥有自己的观察语言\n"
            "--------------------------------\n\n"
            "叙述贴近当前POV人物。\n\n"
            "POV人物观察世界时，应自然带有：\n\n"
            "- 他的经验；\n"
            "- 他的偏见；\n"
            "- 他的知识水平；\n"
            "- 他的幽默感；\n"
            "- 他的误判方式；\n"
            "- 他最先关注的东西。\n\n"
            "不要让所有人物都使用同一种聪明、冷静、精确的旁白语言。\n\n"
            "幽默优先来自人物自己的理解体系。\n\n"
            "人物的私人表达体系必须来自已有设定，不得临时增加网络梗或口癖。\n\n"
            "--------------------------------\n"
            "3. 幽默必须依附事件\n"
            "--------------------------------\n\n"
            "幽默优先来自：\n\n"
            "- 人物误判；\n"
            "- 信息差；\n"
            "- 身份和处境错位；\n"
            "- 严肃人物处理荒唐问题；\n"
            "- 两个人对同一句话理解不同；\n"
            "- 期待与结果相反；\n"
            "- 人物的行动暴露了他嘴上不承认的东西。\n\n"
            "笑点出现后，立即让事件继续推进。\n\n"
            "不要：\n\n"
            "- 解释笑点；\n"
            "- 连续追加三句吐槽；\n"
            "- 让旁白独立于事件讲段子；\n"
            "- 让所有角色都使用相同吐槽方式；\n"
            "- 为了搞笑破坏人物智力和世界规则。\n\n"
            "--------------------------------\n"
            "4. 对白就是行动\n"
            "--------------------------------\n\n"
            "关键对白至少承担一种功能：\n\n"
            "- 提供新事实；\n"
            "- 提出要求；\n"
            "- 拒绝要求；\n"
            "- 试探底线；\n"
            "- 制造误会；\n"
            "- 隐瞒事实；\n"
            "- 作出决定；\n"
            "- 改变关系；\n"
            "- 迫使对方行动。\n\n"
            "对白结束后，局面必须发生变化。\n\n"
            "人物可以：\n\n"
            "- 打断；\n"
            "- 答非所问；\n"
            "- 只回答一部分；\n"
            "- 先做动作再回答；\n"
            "- 用命令包装关心；\n"
            "- 用吐槽掩饰尴尬；\n"
            "- 用沉默拒绝；\n"
            "- 用实际行动代替口头答应。\n\n"
            "禁止通过对白完整朗读：\n\n"
            "- 世界设定；\n"
            "- 人物背景；\n"
            "- 双方都知道的信息；\n"
            "- 人物全部心理；\n"
            "- 作者希望读者理解的结论。\n\n"
            "--------------------------------\n"
            "5. 判断必须尽快转化成行动\n"
            "--------------------------------\n\n"
            "人物形成判断后，不要继续用多个段落反复证明。\n\n"
