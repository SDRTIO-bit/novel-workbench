        "对每个主要角色，必须在 characters 中体现：\n"
        "- observed_evidence：角色在本场景中观察到的具体证据\n"
        "- current_interpretation：角色基于证据形成的当前理解（可能正确也可能错误）\n"
        "- how_interpretation_drives_action：这个理解如何驱动角色的下一步行动\n"
        "这条链必须可追溯：证据 → 理解 → 行动。不得跳过中间环节。\n\n"
        "## 四、具体问题与选择（concrete_problem）\n"
        "concrete_problem 必须是一句话描述角色在本场景中面对的具体、可操作的问题。\n"
        "不得是抽象主题（如“信任危机”），必须是角色此刻必须回答或解决的具体事情。\n\n"
        "## 五、因果转折（causal_transitions）\n"
        "至少生成 1 张 causal_transitions，最多 3 张，不得凑数。\n"
        "每张转折卡必须包含：\n"
        "- id、kind（evidence_to_action 或 constraint_to_choice）\n"
        "- visible_trigger：正文中可被当前视角观察到的具体事实\n"
        "- character_interpretation：角色对这个触发事件的理解（可能带偏差）\n"
        "- character_next_action：角色基于理解采取的下一步行动\n"
        "- rejected_alternative：角色拒绝的替代方案，证明选择不是唯一的\n"
        "- immediate_consequence：行动造成的直接后果\n"
        "- counterfactual_without_action：如果角色不行动，会发生什么\n"
        "- consequence_would_still_happen：如果不行动，immediate_consequence 是否仍会发生。必须填 false。如果为 true，说明该因果卡无效，必须重新规划。\n"
        "- state_delta：{before, after} 行动前后的状态变化，两者必须不同且非空\n"
        "- cost_or_commitment：角色为这个选择付出的代价或做出的承诺\n"
        "- next_constraint：后果产生的新限制，必须与 already_existing_constraints 不同\n"
        "- reader_must_infer：必须留给读者自行连接的推论\n"
        "- narrator_must_not_state（至少 1 项）：叙述者不得直接说出的内容\n\n"
        "因果转折失效条件：\n"
        "1. consequence_would_still_happen 为 true 时，该因果卡无效，必须重新规划。\n"
        "2. 如果 next_constraint 已存在于 already_existing_constraints，则该因果卡无效。\n"
        "3. 如果 state_delta.before 和 after 相同或为空，则该因果卡无效。\n\n"
        "不得把 reader_must_infer 写成角色必然知道的正确答案；人物可以误判。\n"
        "不得以抽象情绪或主题充当 visible_trigger、immediate_consequence 或 next_constraint。\n\n"
        "## 六、节奏与停止（tempo_guardrails）\n"
        "tempo_guardrails 可为空；需要时须包含：\n"
        "- entry_pressure：开场正在发生的具体行动\n"
        "- dominant_pressure：{kind, description} 本场景的主导压力\n"
        "  dominant_pressure.kind 必须原样填写以下一个英文值：physical_problem、social_friction、resource_constraint、unfinished_commitment、information_gap、none。\n"
        "  kind 是供程序读取的单一机器分类，只能选择一个值。不得填写中文，不得填写说明句，不得使用“+”“/”“与”等方式组合多个分类。不得组合多个分类。\n"
        "  如果场景同时存在多种压力，选择真正改变人物下一步行动的主导压力，其他压力写入 description。\n"
        "  physical_problem：人物必须立即处理的物理故障、伤害、阻挡或环境问题。\n"
        "  social_friction：人物之间的尴尬、戒备、冲突、误解或沟通障碍。\n"
        "  resource_constraint：金钱、时间、工具、空间、机会等资源不足。\n"
        "  unfinished_commitment：人物此前的承诺、任务或责任尚未完成。\n"
        "  information_gap：缺少决定下一步行动所必需的信息。\n"
        "  none：本场景没有明确主导压力。\n"
        "- allowed_viewpoint_misread：允许的视角误读\n"
        "- disclosure_cap：披露上限（0 或 1）\n"
        "- must_remain_unclassified：本章不得命名或解释的事实\n"
        "- stop_state：{type, visible_fact, what_is_now_different, must_not_append} 证明局面已变化的停止条件\n"
        "  stop_state.type 必须原样填写以下一个英文值：physical_change、social_commitment、relationship_shift、information_conflict、unresolved_problem。\n"
        "  type 是供程序读取的机器分类，只能选择一个值。不得填写 situational、emotional、open_ending、cliffhanger、other，不得填写中文或自行创造新类型。\n"
        "  根据 stop_state.visible_fact 实际证明的变化选择类型。\n"
        "  physical_change：物体、位置、环境或身体状态发生了可见改变。\n"
        "  social_commitment：人物作出了承诺、接受了责任或形成了必须履行的约定。\n"
        "  relationship_shift：人物之间的距离、信任、立场或互动资格发生改变。\n"
        "  information_conflict：新出现的可见事实与人物原有理解产生冲突。\n"
        "  unresolved_problem：具体问题仍未解决，但已经形成新的行动限制。\n"
        "- final_line_must_include（可选）：必须保留在最后一个非空段的精确短语\n\n"
        "以下例子只用于固定字段格式，不得复用其中的情节：\n"
        "{\n"
        "  \"tempo_guardrails\": {\n"
        "    \"entry_pressure\": \"关店时间已到，女孩仍站在门外\",\n"
        "    \"dominant_pressure\": {\n"
        "      \"kind\": \"social_friction\",\n"
        "      \"description\": \"时间正在减少，但真正阻止行动的是双方尚未建立信任\"\n"
        "    },\n"
        "    \"allowed_viewpoint_misread\": \"女孩可能把老陈的退后理解为不欢迎她\",\n"
        "    \"disclosure_cap\": 1,\n"
        "    \"must_remain_unclassified\": [\n"
        "      \"女孩哭泣的真正原因\"\n"
        "    ],\n"
        "    \"stop_state\": {\n"
        "      \"type\": \"relationship_shift\",\n"
        "      \"visible_fact\": \"女孩把书包放在门内的凳子旁\",\n"
        "      \"what_is_now_different\": \"她从随时准备离开变成暂时接受书店作为落脚处\",\n"
        "      \"must_not_append\": \"不得解释她为什么最终留下\"\n"
        "    }\n"
        "  }\n"
        "}\n\n"
        "## 严格有限视角规划\n\n"
        "本场必须固定一个POV人物。\n\n"
        "Planner可以掌握其他人物的真实目标和隐藏信息，但必须区分：\n\n"
        "- BACKSTAGE_BEHAVIOR_ONLY：\n"
        "  只用于控制该角色如何说话、拒绝、行动或隐瞒。\n\n"
        "- DIRECTLY_NARRATABLE：\n"
        "  允许Writer通过当前POV直接表达的内容。\n\n"
        "其他人物的真实心理、隐藏动机和未公开计划不得进入DIRECTLY_NARRATABLE。\n\n"
        "正文前台只能直接表达：\n\n"
        "- POV人物当前感官；\n"
        "- POV人物明确记忆；\n"
        "- POV人物当下情绪；\n"
        "- POV人物基于已有证据形成的判断；\n"
        "- POV人物允许出现的误判。\n\n"
        "正文前台不得直接表达：\n\n"
        "- 其他人物未说出口的心理；\n"
        "- 其他人物真实动机；\n"
        "- 群体共同感受；\n"
        "- 双方都明白什么；\n"
        "- 关系已经发生什么抽象变化；\n"
        "- 未来结果；\n"
        "- POV人物不在场时发生的事情。\n\n"
        "一个场景内不得切换进入第二个人物内心。\n\n"
        "## 场景容量规划\n\n"
        "target_length不是要求Writer灌水的数字。\n\n"
        "Planner必须先证明本章有足够真实内容支撑target_length。\n\n"
        "Planner必须规划：\n\n"
        "- 真实信息变化；\n"
        "- 人物选择；\n"
        "- 实际阻力；\n"
        "- 对方反馈；\n"
        "- 行动调整；\n"
        "- 即时后果；\n"
        "- 可见stop_state。\n\n"
        "不得通过拆分动作伪造beat数量。\n\n"
        "不得把以下内容当成容量：\n\n"
        "- 同一物件多次观察；\n"
        "- 同一时间多次确认；\n"
        "- 同一判断反复推演；\n"
        "- 按钮操作逐帧拆分；\n"
        "- 环境描写；\n"
        "- 微动作；\n"
        "- stop_state后的余韵。\n\n"
        "当场景容量不足时，优先在核心事件内部增加：\n\n"
        "- 询问；\n"
        "- 试探；\n"
        "- 拒绝；\n"
        "- 误判反馈；\n"
        "- 证据获取；\n"
        "- 现实障碍；\n"
        "- 方法调整；\n"
        "- 直接后果。\n\n"
        "不得向核心事件之外增加无关支线。\n\n"
        "## 互动优先规划\n\n"
        "当两个或以上人物在场，且彼此目标、信息或立场不完全一致时：\n\n"
        "必须优先让核心推进通过人物互动发生。\n\n"
        "Planner需要规划：\n\n"
        "对话或行动触发\n"
        "→ 对方回应\n"
        "→ 新信息或新阻力\n"
        "→ 人物改变下一步行动\n\n"
        "不要规划：\n\n"
        "POV人物独自观察\n"
        "→ 独自列举所有可能\n"
        "→ 独自完成全部推理\n"
        "→ 对方只在旁边充当道具\n\n"
        "## 停止纪律\n\n"
        "stop_state应当发生在最后一个必要推进点。\n\n"
        "不得让stop_state在章节前半段提前成立，然后依靠吃饭、走路、洗碗、看手机、环境描写补足篇幅。\n\n"
        "不得把stop_state后的第二次确认规划成独立beat。\n\n"
        "## 七、契约检查（chapter_contract_check）\n"
        "逐项检查场景规划是否对齐本章契约，所有字段为 bool。只有真正满足时才能填 true：\n"
        "- function_aligned、must_deliver_covered、must_not_deliver_respected、main_change_enabled、main_payoff_prepared、ending_hook_established、causal_transitions_grounded、reader_inference_not_pre_resolved、scene_state_reconstructed、information_sources_legal、character_choice_is_real、consequence_is_counterfactual、state_delta_is_nonempty、next_constraint_is_new、stop_state_is_visible、stop_state_changes_future_actions、pov_character_is_fixed、narration_permissions_are_separated、other_minds_are_backstage_only、no_viewpoint_switch、scene_capacity_is_honest、meaningful_beats_are_non_micro、target_length_has_real_fuel、interaction_changes_state、dialogue_does_not_repeat_shared_information、stop_state_is_not_early\n\n"
        "输出 JSON 字段：\n"
